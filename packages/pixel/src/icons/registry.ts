export type PixelIconEntry = (typeof pixelIconRegistry)[number];
export type PixelIconName = PixelIconEntry['name'];

const pixelIconByName = new Map<PixelIconName, PixelIconEntry>(
  pixelIconRegistry.map((icon) => [icon.name, icon]),
);

export function getPixelIconDefinition(name: string): PixelIconEntry | undefined {
  return pixelIconByName.get(name as PixelIconName);
}
